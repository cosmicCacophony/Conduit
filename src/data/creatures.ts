export function instantiateCreature(template: CreatureTemplate): Creature {
  return {
    ...template,
    currentHp: template.maxHp,
    shield: 0,
    weakened: 0,
    rallied: 0,
    poison: 0,
    poisonTurns: 0,
    possibleIntents: undefined,
    lastStandUsed: false,
    specials: template.specials.map((special) => ({
      ...special,
      currentCooldown: 0,
    })),
  }
}
